public class Location {
    private int row;
    private int column;

    public Location(int row, int column) {
        this.row = row;
        this.column = column;
    }

    /********************************************************
     *                                                      *
     *      DO NOT MODIFY CODE ABOVE THIS BOX              *
     *                                                      *
     ********************************************************/

    // create getter for row here
    public int getRow() {
        return this.row;
    }
    // create getter for column here
    public int getColumn() {
        return this.column;
    }
}